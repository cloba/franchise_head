package org.ksmart.franchise.head.royaltyExpected.model;

public class RoyaltyExpected {
	
	private String lastMonth;
	private String royaltyExpectedCode;
	private String contractCode;
	private String royaltyExpectedStatus;
	private String royaltyExpectedPaid;
	private String royaltyExpectedPayDate;
	private String royaltyExpectedRest;

	
	

	
	public String getContractCode() {
		return contractCode;
	}

	public void setContractCode(String contractCode) {
		this.contractCode = contractCode;
	}

	public String getRoyaltyExpectedStatus() {
		return royaltyExpectedStatus;
	}

	public void setRoyaltyExpectedStatus(String royaltyExpectedStatus) {
		this.royaltyExpectedStatus = royaltyExpectedStatus;
	}

	public String getRoyaltyExpectedPaid() {
		return royaltyExpectedPaid;
	}

	public void setRoyaltyExpectedPaid(String royaltyExpectedPaid) {
		this.royaltyExpectedPaid = royaltyExpectedPaid;
	}

	public String getRoyaltyExpectedPayDate() {
		return royaltyExpectedPayDate;
	}

	public void setRoyaltyExpectedPayDate(String royaltyExpectedPayDate) {
		this.royaltyExpectedPayDate = royaltyExpectedPayDate;
	}

	public String getRoyaltyExpectedRest() {
		return royaltyExpectedRest;
	}

	public void setRoyaltyExpectedRest(String royaltyExpectedRest) {
		this.royaltyExpectedRest = royaltyExpectedRest;
	}

	public String getLastMonth() {
		return lastMonth;
	}

	public void setLastMonth(String lastMonth) {
		this.lastMonth = lastMonth;
	}

	public String getRoyaltyExpectedCode() {
		return royaltyExpectedCode;
	}

	public void setRoyaltyExpectedCode(String royaltyExpectedCode) {
		this.royaltyExpectedCode = royaltyExpectedCode;
	}
		
	
	
}	
