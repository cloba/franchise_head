package org.ksmart.franchise.head.returns.model;

public class ReturnsProcess {
	private String returnsCode;
	private String returnsApprovalIdenfity; 
	private String returnsGroupCode;
	private String receivedOrderCode;
	private String headStaffId;
	
	public String getReturnsCode() {
		return returnsCode;
	}
	public void setReturnsCode(String returnsCode) {
		this.returnsCode = returnsCode;
	}
	public String getReturnsApprovalIdenfity() {
		return returnsApprovalIdenfity;
	}
	public void setReturnsApprovalIdenfity(String returnsApprovalIdenfity) {
		this.returnsApprovalIdenfity = returnsApprovalIdenfity;
	}
	public String getReturnsGroupCode() {
		return returnsGroupCode;
	}
	public void setReturnsGroupCode(String returnsGroupCode) {
		this.returnsGroupCode = returnsGroupCode;
	}
	public String getReceivedOrderCode() {
		return receivedOrderCode;
	}
	public void setReceivedOrderCode(String receivedOrderCode) {
		this.receivedOrderCode = receivedOrderCode;
	}
	public String getHeadStaffId() {
		return headStaffId;
	}
	public void setHeadStaffId(String headStaffId) {
		this.headStaffId = headStaffId;
	}
	
	
}
