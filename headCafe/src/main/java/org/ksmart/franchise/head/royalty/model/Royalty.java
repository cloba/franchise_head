package org.ksmart.franchise.head.royalty.model;

public class Royalty {
	
	private String lastMonth;
	private String RoyaltyCode;
	private String contractCode;
	private String RoyaltyStatus;
	private String RoyaltyPaid;
	private String RoyaltyPayDate;
	private String RoyaltyRest;

	
	

	
	public String getContractCode() {
		return contractCode;
	}

	public void setContractCode(String contractCode) {
		this.contractCode = contractCode;
	}

	public String getRoyaltyStatus() {
		return RoyaltyStatus;
	}

	public void setRoyaltyStatus(String RoyaltyStatus) {
		this.RoyaltyStatus = RoyaltyStatus;
	}

	public String getRoyaltyPaid() {
		return RoyaltyPaid;
	}

	public void setRoyaltyPaid(String RoyaltyPaid) {
		this.RoyaltyPaid = RoyaltyPaid;
	}

	public String getRoyaltyPayDate() {
		return RoyaltyPayDate;
	}

	public void setRoyaltyPayDate(String RoyaltyPayDate) {
		this.RoyaltyPayDate = RoyaltyPayDate;
	}

	public String getRoyaltyRest() {
		return RoyaltyRest;
	}

	public void setRoyaltyRest(String RoyaltyRest) {
		this.RoyaltyRest = RoyaltyRest;
	}

	public String getLastMonth() {
		return lastMonth;
	}

	public void setLastMonth(String lastMonth) {
		this.lastMonth = lastMonth;
	}

	public String getRoyaltyCode() {
		return RoyaltyCode;
	}

	public void setRoyaltyCode(String RoyaltyCode) {
		this.RoyaltyCode = RoyaltyCode;
	}
		
	
	
}	
