package org.ksmart.franchise.head.royaltyOverdue.model;

public class RoyaltyOverdue {

	private String royaltyOverdueCode;
	private String contractCode;
	private String royaltyOverdueStatus;
	private String royaltyOverduePaid;
	private String royaltyOverduePayDate;
	private String royaltyOverdueRest;
	
	public String getRoyaltyOverdueCode() {
		return royaltyOverdueCode;
	}
	public void setRoyaltyOverdueCode(String royaltyOverdueCode) {
		this.royaltyOverdueCode = royaltyOverdueCode;
	}
	public String getContractCode() {
		return contractCode;
	}
	public void setContractCode(String contractCode) {
		this.contractCode = contractCode;
	}
	public String getRoyaltyOverdueStatus() {
		return royaltyOverdueStatus;
	}
	public void setRoyaltyOverdueStatus(String royaltyOverdueStatus) {
		this.royaltyOverdueStatus = royaltyOverdueStatus;
	}
	public String getRoyaltyOverduePaid() {
		return royaltyOverduePaid;
	}
	public void setRoyaltyOverduePaid(String royaltyOverduePaid) {
		this.royaltyOverduePaid = royaltyOverduePaid;
	}
	public String getRoyaltyOverduePayDate() {
		return royaltyOverduePayDate;
	}
	public void setRoyaltyOverduePayDate(String royaltyOverduePayDate) {
		this.royaltyOverduePayDate = royaltyOverduePayDate;
	}
	public String getRoyaltyOverdueRest() {
		return royaltyOverdueRest;
	}
	public void setRoyaltyOverdueRest(String royaltyOverdueRest) {
		this.royaltyOverdueRest = royaltyOverdueRest;
	}
	
	
}
